package map;

/**
 * @author Razican (Iban Eguia)
 */
public class Sprite {

	private int	width;
	private int	height;

	/**
	 * @return Width of the sprite
	 */
	public int getWidth()
	{
		return this.width;
	}

	/**
	 * @return Height of the sprite
	 */
	public int getHeight()
	{
		return this.height;
	}
}
